package ex_07_Increment_Decrement_Op;

public class Lab0030_70_ID_OP_PRE {
    public static void main (String[]args){
        int a = 10;
        System.out.println(a); // o/p = 10 before increament
        int b = ++a; // increment o/p = 11

        //increanment the value then print.
        System.out.println(a); // already asigned op = 11
        System.out.println(b);
    }
}
