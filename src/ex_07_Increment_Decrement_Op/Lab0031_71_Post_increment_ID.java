package ex_07_Increment_Decrement_Op;

public class Lab0031_71_Post_increment_ID {
    public static void main(String[] args){
        int a = 11;
        System.out.println(a); //O/p 11
        int b = a++;

        System.out.println(b); // first assign OP=11
        System.out.println(a); //  then increament OP = 12

    }
}
